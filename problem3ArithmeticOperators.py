# Solution:

a = int(input("Enter number a: "))
b = int(input("Enter number b: "))

# task1: Sum

sum = a+b
print(f"The sum of {a} and {b} is, {sum}")

# task2: Difference

min = a-b
print(f"The difference between {a} and {b} is, {min} ")

# task3: multification

mult = a*b
print(f"The multification of {a} and {b} is, {mult} ")